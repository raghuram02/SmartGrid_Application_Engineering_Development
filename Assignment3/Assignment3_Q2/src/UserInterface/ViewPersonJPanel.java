    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter the  name");
            return;
        } else if (!txtAge.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Enter the Age");
            return;
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter the email ID");
            return;

        } else if (txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter the address");
            return;
        } else if (txtZip.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter the zipcode ");
            return;
        } else if (!txtPhone.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Enter the phone number");
            return;
        }
        try {
            person.setName(txtName.getText());
            person.setAge(Integer.parseInt(txtAge.getText()));
            person.setPhoneNumber(Integer.parseInt(txtPhone.getText()));

            person.setEmailId(txtEmail.getText());
            person.setAddress(txtAddress.getText());
            person.setZipcode(txtZip.getText());

            JOptionPane.showMessageDialog(null, "person Details saved successfully",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to save the person details",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        } finally {
            txtName.setEditable(false);

            txtPhone.setEditable(false);
            txtAge.setEditable(false);
            txtEmail.setEditable(false);
            txtAddress.setEditable(false);
            txtZip.setEditable(false);
            btnSave.setEnabled(false);
            btnUpdatePerson.setEnabled(true);

        }
    }//GEN-LAST:event_btnSaveActionPerformed

