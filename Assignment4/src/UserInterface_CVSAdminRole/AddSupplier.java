    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Supplier name");
            return;
        } else if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Supplier ID");
            return;
        } else if (txtPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Supplier Phone number");
            return;}
            Supplier supplier = supplierDirectory.addSupplier();
            supplier.setSupplierName(txtName.getText());
            supplier.setSupplierID(txtID.getText());
            supplier.setSupplierPhone(txtPhone.getText());
            JOptionPane.showMessageDialog(null, "Supplier added successfully", "Information", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnAddActionPerformed

