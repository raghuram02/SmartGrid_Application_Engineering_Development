        private void back() {
            userProcessContainer.remove(this);
            Component[] componentArray = userProcessContainer.getComponents();
            Component manageCustomers = componentArray[componentArray.length - 1];
            ManageCustomers manage = (ManageCustomers) manageCustomers;
            manage.populateTable();
            CardLayout Layout = (CardLayout) userProcessContainer.getLayout();
            Layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnCreateCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateCustomerActionPerformed
 if (txtCName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Customer name");
            return;
        } else if (txtCID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Customer ID");
            return;
        } else if (txtCEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Customer Email ID");
            return;

        } else if (txtCAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a valid Customer Address");
            return;
        } else if (!txtCPhone.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Enter a valid Phone Number");
            return;
        }

        Customer customer = customerDirectory.addCustomer();
        customer.setCustomerName(txtCName.getText());
        customer.setCustomerID(txtCID.getText());
        customer.setEmail(txtCEmail.getText());
        customer.setAddress(txtCAddress.getText());
        customer.setCustomerPhone(Integer.parseInt(txtCPhone.getText()));

        JOptionPane.showMessageDialog(null, "Customer created successfully!!!", "Information", JOptionPane.INFORMATION_MESSAGE);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnCreateCustomerActionPerformed


